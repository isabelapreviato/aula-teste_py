# Dois números

n1 = float(input("Digite o número 1: "))
n2 = float(input("Digite o número 2: "))

soma = n1 + n2
print(f"A soma dos dois números: {soma}")

sub = n1 - n2
print(f"A subtração dos dois números: {sub}")

mult = n1 * n2
print(f"A multiplicação dos dois números: {mult}")

div = n1 / n2
print(f"A divisão dos dois números: {div}")

# Quatro números

nn1 = float(input("Digite o número 1 de 4: "))
nn2 = float(input("Digite o número 2 de 4: "))
nn3 = float(input("Digite o número 3 de 4: "))
nn4 = float(input("Digite o número 4 de 4: "))

soma2 = nn1 + nn2 + nn3 + nn4
print(f"A soma dos quatro números é: {soma2}")

media = soma2 / 4
print(f"A média dos quatro números é: {media}")

